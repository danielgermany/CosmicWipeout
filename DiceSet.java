import java.util.ArrayList;

public class DiceSet {
    // instance variables
    int diceSetSize;
    ArrayList<String> dieInfo;

    // Constructor
    /**
     * @param diceSetSize
     * @param dieInfo
     */
    public DiceSet(int diceSetSize, ArrayList<String> dieInfo) {
        this.diceSetSize = diceSetSize;
        this.dieInfo = dieInfo;
    }

}
